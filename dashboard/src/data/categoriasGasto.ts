export function categoriasDaEntidade(id: EntidadeId): CategoriaGasto[] {
  return CATEGORIAS_GASTO.filter(
    (c) => c.aplicaA.includes(id) || c.aplicaA.includes("ambos"),
  );
}

export function estimativaPara(
  cat: CategoriaGasto,
  id: EntidadeId,
): string | undefined {
  return id === "prefeitura" ? cat.estimativaPrefeitura : cat.estimativaCamara;
}
